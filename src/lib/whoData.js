/** Calcula a idade em meses completos a partir da data de nascimento */
export function idadeEmMeses(dataNascimento) {
  const nasc = new Date(dataNascimento)
  const hoje = new Date()
  return Math.floor((hoje - nasc) / (1000 * 60 * 60 * 24 * 30.44))
}

/** Formata a idade de forma clínica */
export function formatarIdade(dataNascimento) {
  const nasc = new Date(dataNascimento)
  const hoje = new Date()
  const dias = Math.floor((hoje - nasc) / (1000 * 60 * 60 * 24))
  if (dias < 29) return `${dias} dia${dias !== 1 ? 's' : ''}`
  const meses = Math.floor(dias / 30.44)
  if (meses < 24) return `${meses} ${meses === 1 ? 'mês' : 'meses'}`
  const anos = Math.floor(meses / 12)
  const mesesResto = meses % 12
  return mesesResto > 0 ? `${anos}a ${mesesResto}m` : `${anos} anos`
}
